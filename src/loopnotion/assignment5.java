//- **Assignment - 06**
//
//    Print ASCII values and their equivalent characters. ASCII value vary from 0 to 255.
public class assignment5 {
    public static void main(String[] args) {
        for (int i=0; i<=255; i++){
            char ch=(char) i;
            System.out.println(ch);
        }
    }
}
