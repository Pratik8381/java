package operators;

public class IncrementAndDecrementOperators {
    public static void main(String [] arg){
        int a=5;
        int b =++a;
        System.out.println("Enter value of the a"+a);
        System.out.println("Enter value of the b"+b);
        int x =5;
        int y =x++;
        System.out.println("Enter value of the x"+x);
        System.out.println("Enter value of the y"+y);

        int c =5;
        int d =--c;
        System.out.println("Enter value of the c"+c);
        System.out.println("Enter value of the d"+d);

        int e =5;
        int f= e--;
        System.out.println("Enter value of the e"+e);
        System.out.println("Enter value of the f"+f);





    }
}
